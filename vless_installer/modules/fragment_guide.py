    _h("📋 Без фрагментации  (frag_00)")
    _p(
        "Эталонный конфиг. Используйте для сравнения: если он работает "
        "так же хорошо, как конфиги с фрагментацией — фрагментация вам "
        "не нужна вообще. Оставьте его и не усложняйте."
    )

