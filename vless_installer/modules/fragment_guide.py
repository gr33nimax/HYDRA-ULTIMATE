    _step(4, "Отключитесь, переключитесь на следующий конфиг")
    _step(5, "Повторите для всех 9 файлов")

    _h("Шаг 5 — Выберите лучший")
    _p(
        "Оставьте конфиг с наименьшим пингом и наибольшей скоростью. "
        "Если несколько работают одинаково — берите с наибольшим length "
        "(меньше нагрузка на сеть)."
    )

    print()
    input(f"  {BLUE}Нажмите Enter для продолжения...{NC}")


