# ── Главное меню гайда ────────────────────────────────────────────────────

def do_fragment_guide_menu() -> None:
    """
    Интерактивный гайд по тестированию фрагментации.
    Вызывается из _menu_diagnostics() в _core.py.
    """
    while True:
        os.system("clear")
        print()
        _box_top("📖  ГАЙД: КАК ТЕСТИРОВАТЬ ФРАГМЕНТАЦИЮ")
        _box_desc(
            "Фрагментацию нужно тестировать на своём устройстве — не на VPS. "
            "Этот гайд объяснит почему и как правильно подобрать параметры."
        )
        _box_sep()
        _box_row()
        _box_item("1", "❓ Почему тестировать надо у себя, а не на VPS")
        _box_item("2", "📋 Пошаговая инструкция: скачать → импортировать → выбрать")
        _box_item("3", "🗂️  Группы пресетов — что выбрать и с чего начать")
        _box_item("4", "📱 Поддержка фрагментации в разных клиентах")
        _box_row()
        _box_back()
        _box_bottom()

        try:
            ch = input(f"{CYAN}Выбор:{NC} ").strip().lower()
        except KeyboardInterrupt:
            break

        if ch == "1":
            _page_why()
        elif ch == "2":
            _page_howto()
        elif ch == "3":
            _page_presets()
        elif ch == "4":
            _page_clients()
        elif ch == "q" or ch == "":
            break
        else:
            pass  # просто перерисовываем меню
