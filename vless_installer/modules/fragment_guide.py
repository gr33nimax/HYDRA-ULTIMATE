    _p("Конфиги отправляются в чат автоматически.", indent=7)

    _h("Шаг 3 — Импортируйте конфиги в клиент")
    print()
    _bullet(f"{WHITE}v2rayNG (Android):{NC}   +  →  Импорт → Из файла конфигурации", "")
    _bullet(f"{WHITE}Nekoray (Windows/Linux):{NC}  File → Import → From File", "")
    _bullet(f"{WHITE}Hiddify (все платформы):{NC}  Новый профиль → Из файла", "")
